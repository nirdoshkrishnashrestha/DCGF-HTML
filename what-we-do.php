<p style="font-weight: 400;"><strong style="font-weight: 600;"><u>Deposit Guarantee</u></strong></p>
<p style="font-weight: 400;">Deposit Guarantee is a program run by the government with a commitment to provide protection for the savings of general public so as to build confidence of the public over the banking system and bring the money scattered among the public into a proper banking channel to utilize it for the economic development of the country.</p>
<p style="font-weight: 400;"><strong style="font-weight: 600;"><u>Necessity and Importance of Deposit Guarantee</u></strong></p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;a)&nbsp;&nbsp;To protect depositor's interests,</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;b)&nbsp; &nbsp;For stability in financial system,</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;c)&nbsp;&nbsp;&nbsp;To protect deposits of small depositors,</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;d)&nbsp;&nbsp;&nbsp;To increase competitiveness in financial system, and</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;e)&nbsp;&nbsp;&nbsp;To reduce the use of state funds in financial rescue works.</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong style="font-weight: 600;"><u>Credit Guarantee</u></strong></p>
<p style="font-weight: 400;">Credit Guarantee is a program run by the nation for increasing financial access to the poor, privileged, marginalized and deprived sector people lacking source of fund with lot of zeal via bank and financial institutions with a commitment to provide protection against the investment of bank and financial institutions under those sectors.</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong style="font-weight: 600;"><u>Necessity and Importance of Credit Guarantee</u></strong></p>
<ol style="font-weight: 400;">
<li>To encourage banks to invest in risky and deprived sectors/areas.</li>
<li>To make arrangements that can supplement financial access from the backward region of the nation.</li>
<li>To provide banking facilities for the deprived sectors.</li>
<li>To minimize the problem of capital scarcity in banks,</li>
<li>For creation of self-employment opportunities,</li>
<li>It helps in poverty alleviation.</li>
<li>To support in the overall economic development of nation.</li>
</ol>
